package com.syntax.class02;

public class Task1 {

	public static void main(String[] args) {

		double a = 4.76, b = 3.69;

		double sum = a + b;
		double sub = a - b;
		double mul = a * b;
		double div = a / b;

		System.out.println("The addition of two numbers " + a + " and " + b + " is equal to " + sum);
		System.out.println("The subtraction of two numbers " + a + " and " + b + " is equal to " + sub);
		System.out.println("The multiplication of two numbers " + a + " and " + b + " is equal to " + mul);
		System.out.println("The division of two numbers " + a + " and " + b + " is equal to " + div);

	}

}
