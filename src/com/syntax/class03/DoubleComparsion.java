package com.syntax.class03;

public class DoubleComparsion {

	public static void main(String[] args) {

		double myNum=36.65;
		double myNum1=78.55;
		
		if (myNum>myNum1) {
			System.out.println("Double value "+myNum+" is larger than "+myNum1+".");
		}else {
			System.out.println("Double value "+myNum+" is smaller than "+myNum1+".");
		}
		
		
	}

}
